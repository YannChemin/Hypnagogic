// Hyperspectral vector structure
typedef struct {
    float* wavelengths;
    float* reflectance;
    int size;
    MaterialType material;
    char name[64];
} HyperspectralVector;

// Reference material data structure for header file storage
typedef struct {
    const char* name;
    MaterialType material;
    int size;
    const float* wavelengths;
    const float* reflectance;
} ReferenceMaterialData;

